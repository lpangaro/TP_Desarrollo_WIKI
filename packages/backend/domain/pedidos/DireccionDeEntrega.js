export class DireccionDeEntrega {
    constructor(calle, altura, departamento, codigoPostal, ciudad, provincia, pais, lat, lon) {
        this.calle = calle;
        this.altura = altura;
        this.departamento = departamento;
        this.codigoPostal = codigoPostal;
        this.ciudad = ciudad;
        this.provincia = provincia;
        this.pais = pais;
        this.lat = lat;
        this.lon = lon;
    }
}
